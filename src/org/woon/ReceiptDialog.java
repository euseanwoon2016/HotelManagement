    private void calculateTotal(BookingData bookingData) {
        double totalRoomPrice = bookingData.getDaysBooked().length * bookingData.getRoomPrice();
        model.addRow(new Object[]{
            "Room " + bookingData.getRoomNumber(),
            bookingData.getDaysBooked().length + " days",
            numFmt.format(bookingData.getRoomPrice()),
            numFmt.format(totalRoomPrice)
        });

        double serviceTax = totalRoomPrice * 0.10;
        model.addRow(new Object[]{
            "Service Tax (10%)",
            "1",
            "",
            numFmt.format(serviceTax)
        });

        double tourismTax = (bookingData.getDaysBooked().length * 10);
        model.addRow(new Object[]{
            "Tourism Tax",
            bookingData.getDaysBooked().length + " days",
            numFmt.format(10),
            numFmt.format(tourismTax)
        });

        double total = totalRoomPrice + serviceTax + tourismTax;
        model.addRow(new Object[]{
            "",
            "",
            "Total",
            numFmt.format(total)
        });

        model.fireTableDataChanged();
    }
