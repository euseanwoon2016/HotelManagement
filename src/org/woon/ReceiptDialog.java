class ReceiptTableModel extends AbstractTableModel {

    private String[] columnNames = {"Item Name", "Quantity", "Unit Price", "Price"};
    private Object[][] data = new Object[0][4];

    @Override
    public boolean isCellEditable(int rowIndex, int columnIndex) {
        return false;
    }

    @Override
    public String getColumnName(int column) {
        return columnNames[column];
    }

    @Override
    public int getRowCount() {
        return data.length;
    }

    @Override
    public int getColumnCount() {
        return columnNames.length;
    }

    @Override
    public Object getValueAt(int rowIndex, int columnIndex) {
        return data[rowIndex][columnIndex];
    }

    public void setValueAt(Object value, int row, int col) {
        data[row][col] = value;
        fireTableCellUpdated(row, col);
    }

    public void addRow(Object[] object) {
        Object[][] newData = (Object[][]) Array.newInstance(Object.class, getRowCount() + 1, getColumnCount() + 1);
        for (int i = 0; i < data.length; i++) {
            for (int j = 0; j < columnNames.length; j++) {
                newData[i][j] = data[i][j];
            }
        }
        newData[getRowCount()] = object;
        data = newData;

        fireTableDataChanged();
    }

    public void setRow(Object[] object, int rowIndex) {
        for (int i = 0; i < object.length; i++) {
            data[rowIndex][i] = object[i];
        }
    }

}
